<?php
namespace AppBundle\Model;

class Stage{
    
    private $_id;
    private $_id_Company;
    private $_id_Student;
    private $_date_debut;
    private $_date_fin;
    private $convention;
    
    public function __construct($id,$id_Company,$id_Student,$date_debut,$date_fin,$convention){
        $this->_id=$id;
        $this->_id_Company=$id_Company;
        $this->_id_Student=$id_Student;
        $this->_date_debut=$date_debut;
        $this->_date_fin=$date_fin;
        $this->_convention=$convention;
    }
    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->_id;
    }

    /**
     * @return mixed
     */
    public function getId_Company()
    {
        return $this->_id_Company;
    }

    /**
     * @return mixed
     */
    public function getId_Student()
    {
        return $this->_id_Student;
    }

    /**
     * @return mixed
     */
    public function getDate_debut()
    {
        return $this->_date_debut;
    }

    /**
     * @return mixed
     */
    public function getDate_fin()
    {
        return $this->_date_fin;
    }
    
    /**
     * @return mixed
     */
    public function getConvention()
    {
        return $this->_convention;
    }
    

    /**
     * @param mixed $_id
     */
    public function setId($_id)
    {
        $this->_id = $_id;
    }

    /**
     * @param mixed $_id_Company
     */
    public function setId_Company($_id_Company)
    {
        $this->_id_Company = $_id_Company;
    }

    /**
     * @param mixed $_id_Student
     */
    public function setId_Student($_id_Student)
    {
        $this->_id_Student = $_id_Student;
    }

    /**
     * @param mixed $_date_debut
     */
    public function setDate_debut($_date_debut)
    {
        $this->_date_debut = $_date_debut;
    }

    /**
     * @param mixed $_date_fin
     */
    public function setDate_fin($_date_fin)
    {
        $this->_date_fin = $_date_fin;
    }
    
    /**
     * @param mixed $_date_fin
     */
    public function setConvention($_convention)
    {
        $this->_convention = $_convention;
    }


    
    
}